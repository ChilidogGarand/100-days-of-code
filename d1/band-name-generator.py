def main():
    # Print a greeting for the program
    print("Welcome to the Band Name Generator! Let's rock.")

    # Ask user for the name of the city they grew up in, input should be added on the line below
    city = input("What's the name of the city where you grew up?\n")

    # Ask user for the name of a pet
    pet = input("What's the name of your pet?\n")

    # Combine the inputs for 'city' and 'pet' to create a band name
    print("A good band name would be " + city + " " + pet + ".")

main()

